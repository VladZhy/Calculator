class Calculator {
    divide(firstNumber, secondNumber) {
        return [firstNumber / secondNumber];
    }

    multiply(firstNumber, secondNumber) {
        return [firstNumber * secondNumber];
    }

    minus(firstNumber, secondNumber) {
        return [firstNumber - secondNumber];
    }

    plus(firstNumber, secondNumber) {
        return [firstNumber + secondNumber];
    }
}

export default Calculator;